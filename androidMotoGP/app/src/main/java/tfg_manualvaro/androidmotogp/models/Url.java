package tfg_manualvaro.androidmotogp.models;

public class Url {

    private String url;



    @Override
    public String toString() {
        return "url{" +
                "url='" + url + '\'' +
                '}';
    }


    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url= url;
    }

}
